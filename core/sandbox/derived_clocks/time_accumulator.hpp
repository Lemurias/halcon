#pragma once

/*******************************************************************************
* STANDARD LIBRARIES
********************************************************************************/

#include <cmath>

/*******************************************************************************
* TIME ACCUMULATOR CLASS
********************************************************************************/

class TimeAccumulator
{
private: 
    volatile long double sum { 0.0 };
    volatile long double cs { 0.0 };
    volatile long double ccs { 0.0 };
    volatile long double c { 0.0 };
    volatile long double cc { 0.0 };

public:
    long double add(long double delta);
};

long double TimeAccumulator::add(long double delta)
{
    /* Kahan Babushka Klein Algorithm */
    volatile long double t = sum + delta;
    c = (std::abs(sum) >= std::abs(delta))? (sum - t) + delta : (delta - t) + sum;
    sum = t;
    t = cs + c;
    cc = (std::abs(cs) >= std::abs(c))? (cs - t) + c : (c - t) + cs;
    cs = t;
    ccs = ccs + cc;
    return sum + cs + ccs;
}