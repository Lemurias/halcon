#include "tictoc.hpp"

/*******************************************************************************
* TIC-TOC CLASS
********************************************************************************/

void TicToc::Tic(std::string key)
{
    tic_map[key] = std::chrono::high_resolution_clock::now();
}

long int TicToc::Toc(std::string key, bool update)
{
    auto t1 = std::chrono::high_resolution_clock::now();

    if (update)
    {
        tic_map[key] = t1;
    }

    auto t_diff_us = std::chrono::duration_cast<std::chrono::microseconds>(t1 - tic_map[key]);    
    return t_diff_us.count();
}

double TicToc::Rate(std::string key, long unsigned int iteration)
{
    double t_diff = static_cast<double>(Toc(key, false)) * 1e-6;
    double t_prom_sec = t_diff  / static_cast<double>(iteration);
    double bitrate = 1.0 / t_prom_sec;
    return bitrate;
}