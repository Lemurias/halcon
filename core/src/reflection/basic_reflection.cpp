#include "basic_reflection.hpp"

/**
 * @brief Returns variable map
 * 
 */
HandlersMap BasicReflection::GetMapPointers(KeyFormat f) const
{
    HandlersMap nested_map;

    /* Name prefix */
    std::string prefix = (f == Full)? full_name + "." : "";

    /* Add variables */
    for (auto it = h_variable_map.begin(); it != h_variable_map.end(); ++it)
    {
        nested_map[prefix + it->first] = it->second;
    }
    return nested_map;
}

/**
 * @brief Returns variable map
 * 
 */
std::map<std::string, std::string> BasicReflection::GetMapTypes(KeyFormat f) const
{
    std::map<std::string, std::string> nested_map;

    /* Name prefix */
    std::string prefix = (f == Full)? full_name + "." : "";

    /* Add variables */
    for (auto it = h_variable_map.begin(); it != h_variable_map.end(); ++it)
    {
        nested_map[prefix + it->first] = "*variable_" + it->second->GetTypeAsString();
    }
    return nested_map;
}