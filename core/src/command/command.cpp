/**
 * @brief Command initialization based on command_string line
 *
 * @return bool
 */
void Command::Parse(const std::string& command)
{
    /* Save command */
    command_string = command;

    /* Clear line */
    command_string = command_string.substr(0, command_string.find("#", 0));

    /* Try to parse command */
    try
    {
        app->parse(command_string);
    }
    catch(const std::exception& e)
    {
        std::string error_text = std::string(__FILE__) + ":"
                               + std::to_string(__LINE__) + ": "
                               + "ERROR [command parser error]: " 
                               + std::string(e.what());
        throw std::runtime_error(error_text);
    }
}