#include "nameable.hpp"

/**
 * @brief Returns the name of the object
 * 
 * @return std::string 
 */
const std::string& Nameable::GetName() const
{
    return name;
}

/**
 * @brief Returns the full name of the object (full hierarchy)
 * 
 * @return std::string 
 */
const std::string& Nameable::GetFullName() const
{
    return full_name;
}

/**
 * @brief Sets object name
 * 
 * @param name 
 */
void Nameable::SetName(std::string new_name)
{
    this->name = new_name;
    this->was_named = true;
}

/**
 * @brief Sets name and full name of the object
 * 
 * @param top_name 
 * @param name 
 */
void Nameable::SetFullName(std::string top_name, std::string new_name)
{
    this->name = new_name;
    this->full_name = top_name + "." + new_name;
    this->was_fully_named = true;
}

const bool& Nameable::WasNamed() const
{
    return was_named;
}

const bool& Nameable::WasFullyNamed() const
{
    return was_fully_named;
}
