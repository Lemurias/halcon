#include "template.hpp"

Template::Template()
{
    /* Modules */

    /* Registers */
    REFLECT(r_out);
    
    /* Ports */
    REFLECT(i_clock);
    REFLECT(i_signal_a);
    REFLECT(i_signal_b);
    REFLECT(o_sum);
    REFLECT(o_mult);

    /* Variables */
    REFLECT(sum);
    
    /* Settings YAML */
    REFLECT_YAML(factor);
}

void Template::Init()
{
    /* pass */
}

void Template::Connect()
{
    /* Registers to Clock */
    i_clock->RegisterOnPositiveEdge(this, r_out);

    /* Combinational Ports */
    o_sum << sum << COMBINATIONAL_PORT;
    
    /* Sequential Ports */
    o_mult << r_out.o;
}

void Template::RunClockMaster()
{
    /* Sum */
    sum = i_signal_a.GetData() + i_signal_b.GetData();
    
    /* Multiplication */
    r_out.i = sum * factor;
}