import numpy as np
from scipy.special import erfc

def qam_ber_awgn(ebno_db_v, m_qam):
    ebno_db_v = np.asarray(ebno_db_v, dtype=np.float64)
    k_qam = np.log2(m_qam)
    ebno = 10**(ebno_db_v / 10.0)
    
    ber = (2 / k_qam) \
        * (1 - 2**(- k_qam / 2)) \
        * erfc(np.sqrt((3 * k_qam * ebno) / (2 * (2**k_qam - 1))))
    
    if ber.size == 1:
        return ber.item()
    return ber.tolist()